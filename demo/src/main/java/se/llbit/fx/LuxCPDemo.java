package se.llbit.fx;

import javafx.application.Application;
import javafx.fxml.FXMLLoader;
import javafx.scene.Parent;
import javafx.scene.Scene;
import javafx.stage.Stage;

/**
 * Demo application for LuxColorPicker.
 *
 * <p>The application only has a main window with a singular color picker
 * button that sets the background color of the window.
 */
public class LuxCPDemo extends Application {
  public static void main(String[] args) {
    Application.launch(LuxCPDemo.class, args);
  }

  @Override public void start(Stage stage) throws Exception {
    Parent root = FXMLLoader.load(getClass().getResource("LuxCPDemo.fxml"));
    stage.setTitle("LuxColorPicker Demo");
    stage.setScene(new Scene(root));
    stage.show();
  }
}
